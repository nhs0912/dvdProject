package vo;

public class Price {
	int price_no;//가격정보 번호 
	String price_content; //가격 정보
	String price;//가격 
	public int getPrice_no() {
		return price_no;
	}
	public void setPrice_no(int price_no) {
		this.price_no = price_no;
	}
	public String getPrice_content() {
		return price_content;
	}
	public void setPrice_content(String price_content) {
		this.price_content = price_content;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	
}
